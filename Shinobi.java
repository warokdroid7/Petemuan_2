public class Shinobi
{
	private String shinobiName;
	private String villageFrom;
	private double speed;
	private int power;

	public void setShinobiName(String shinobiName){
		this.shinobiName = shinobiName;
	}
	
	public String getShinobiName(){
		return shinobiName;
	}
	
	public void setVillageFrom(String villageFrom){
		this.villageFrom = villageFrom;
	}
	
	public String getVillageFrom(){
		return villageFrom;
	}
	
	public void setSpeed(double speed){
		this.speed = speed;
	}
	
	public double getSpeed(){
		return speed;
	}
	
	public void setPower(int power){
		this.power = power;
	}
	
	public int getPower(){
		return power;
	}
	
	
}
